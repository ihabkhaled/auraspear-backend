/**
 * Configurable roles are derived from the canonical hierarchy plus the
 * default-permissions map so newly-seeded roles appear automatically
 * in the role settings matrix without maintaining a second manual list.
 */
export const CONFIGURABLE_ROLES: string[] = ROLE_HIERARCHY.filter(
  role =>
    role !== UserRole.GLOBAL_ADMIN &&
    Object.prototype.hasOwnProperty.call(DEFAULT_PERMISSIONS, role)
)
